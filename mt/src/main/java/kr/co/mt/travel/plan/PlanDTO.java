package kr.co.mt.travel.plan;

public class PlanDTO {

	private String plan_no;
	private String cate_no;
	private String plan_name;
	private String plan_desc;
	private String plan_amount;
	private String plan_date;
	private String plan_loc;
	private String mno;
	
	public String getPlan_no() {
		return plan_no;
	}
	public void setPlan_no(String plan_no) {
		this.plan_no = plan_no;
	}
	public String getCate_no() {
		return cate_no;
	}
	public void setCate_no(String cate_no) {
		this.cate_no = cate_no;
	}
	public String getPlan_name() {
		return plan_name;
	}
	public void setPlan_name(String plan_name) {
		this.plan_name = plan_name;
	}
	public String getPlan_desc() {
		return plan_desc;
	}
	public void setPlan_desc(String plan_desc) {
		this.plan_desc = plan_desc;
	}
	public String getPlan_amount() {
		return plan_amount;
	}
	public void setPlan_amount(String plan_amount) {
		this.plan_amount = plan_amount;
	}
	public String getPlan_date() {
		return plan_date;
	}
	public void setPlan_date(String plan_date) {
		this.plan_date = plan_date;
	}
	public String getPlan_loc() {
		return plan_loc;
	}
	public void setPlan_loc(String plan_loc) {
		this.plan_loc = plan_loc;
	}
	public String getMno() {
		return mno;
	}
	public void setMno(String mno) {
		this.mno = mno;
	}
	
}
