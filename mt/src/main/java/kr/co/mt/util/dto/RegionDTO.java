package kr.co.mt.util.dto;

public class RegionDTO {
	private String region_no;
	private String region_name;
	private String region_photoname;
	
	public String getRegion_no() {
		return region_no;
	}
	public void setRegion_no(String region_no) {
		this.region_no = region_no;
	}
	public String getRegion_name() {
		return region_name;
	}
	public void setRegion_name(String region_name) {
		this.region_name = region_name;
	}
	public String getRegion_photoname() {
		return region_photoname;
	}
	public void setRegion_photoname(String region_photoname) {
		this.region_photoname = region_photoname;
	}
}
