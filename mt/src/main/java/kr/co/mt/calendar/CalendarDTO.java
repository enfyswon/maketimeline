package kr.co.mt.calendar;

public class CalendarDTO {

	private String to_date;
	private String last_date;
	private String first_day;
	private String for_end;
	private String this_month;
	private String this_year;

	public String getTo_date() {
		return to_date;
	}
	public void setTo_date(String to_date) {
		this.to_date = to_date;
	}
	public String getLast_date() {
		return last_date;
	}
	public void setLast_date(String last_date) {
		this.last_date = last_date;
	}
	public String getFirst_day() {
		return first_day;
	}
	public void setFirst_day(String first_day) {
		this.first_day = first_day;
	}
	public String getFor_end() {
		return for_end;
	}
	public void setFor_end(String for_end) {
		this.for_end = for_end;
	}
	public String getThis_month() {
		return this_month;
	}
	public void setThis_month(String this_month) {
		this.this_month = this_month;
	}
	public String getThis_year() {
		return this_year;
	}
	public void setThis_year(String this_year) {
		this.this_year = this_year;
	}

}//class

